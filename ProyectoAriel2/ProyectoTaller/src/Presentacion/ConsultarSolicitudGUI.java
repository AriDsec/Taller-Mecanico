    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String estadoSeleccionado = (String) comboBoxEstado.getSelectedItem();

        HashMap<String, String> estados = accesoXML.leerEstados();
        String idEstado = null;

        // Buscar el ID del estado a partir de su nombre
        for (Map.Entry<String, String> entry : estados.entrySet()) {
            if (entry.getValue().equals(estadoSeleccionado)) {
                idEstado = entry.getKey();
                break; // Terminar el bucle una vez encontrado el ID
            }
        }

        if (idEstado != null) {
            Solicitud.consultar(tablaSolicitudes, idEstado, txtTelefono.getText(), txtPlaca.getText(), txtEmail.getText()); // Llama a consultar con el ID del estado
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

